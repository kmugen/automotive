#include "gpio.h"
#include "IfxCpu.h"

void setPinMode(gpio_t pin, uint8 mode)
{
    volatile Ifx_P_IOCR0 *iocr    = &(pin.port->IOCR0);
    uint8            iocrIdx = (pin.pinIndex / 4);
    uint8            pcIdx   = (pin.pinIndex % 4);

    iocr[iocrIdx].U &= ~(0xFF << (pcIdx * 8));
    iocr[iocrIdx].U |=  (mode << (pcIdx * 8));
}
